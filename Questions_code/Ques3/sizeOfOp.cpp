/*Ques: How much space does following data types take ?

int
bool
float
*/
#include<iostream>
using namespace std;
int main()
{
  int x;
  cout<<"Size of int data type:"<<sizeof(x)<<endl;
  bool y;
  cout<<"Size of bool data type:"<<sizeof(y)<<endl;
  float z;
  cout<<"Size of float data type:"<<sizeof(z)<<endl;

}